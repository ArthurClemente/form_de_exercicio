function esconderIdade() {
    let p_idade = document.getElementById("p_idade");
    let idade = document.getElementById("idade");

    p_idade.hidden = true;
    idade.hidden = true;
}

function mostrarIdade() {
    let p_idade = document.getElementById("p_idade");
    let idade = document.getElementById("idade");

    p_idade.hidden = false;
    idade.hidden = false;
}

